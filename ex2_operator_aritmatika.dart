void main(List<String> args) {
  double a = 10, b = 3; // Menggunakan tipe data double untuk a dan b
  print('\n$a + $b = ${a + b}');
  print('$a - $b = ${a - b}');
  print('$a * $b = ${a * b}');
  print('$a / $b = ${a / b}');
  print('$a ~/ $b = ${a ~/ b}');
  print('$a % $b = ${a % b}');
}
